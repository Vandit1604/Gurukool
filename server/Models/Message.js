const mongoose = require("mongoose");
//defining Message schema
const MessageSchema = new mongoose.Schema(
  {
    conversationId: {
      type: String,
      required: true,
    },
    sender: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
//exporting the Message model
module.exports = new mongoose.model("Message", MessageSchema);
